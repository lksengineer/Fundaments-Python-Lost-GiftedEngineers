#Slices
b = "Hello, World!"
print(b[-5:-2]) #Display from -5 to -2

b = "Hello, World!"
print(b[::-1]) #Display reversing

b = "Hello, World!"
print(b[::2]) #display 2 in 2

#strip()
a = " Hello World! "
print(a.strip()) # returns "Hello, World!"

#upper()
a = "hello".upper()
print(a)

#lower()
a = "Hello".lower()
print(a)

#replace()
a = "hello"
print(a.replace("l", "r"))

#split()
a = "hello world"
print(a.split(" "))