# Evaluate True values
print("*****True values*****")
print(bool("Hello"))
print(bool(15))

x = "Hello"
y = '15'

print(bool(x))
print(bool(y))

# Evaluate False values
print("*****False values*****")
print(bool(False))
print(bool(None))
print(bool(0))
print(bool(""))
print(bool(()))
print(bool([]))
print(bool({}))